# Напишете програма, която изчислява каква сума ще получите в края на депозитния период при определен лихвен процент.
# Използвайте следната формула:
# сума = депозирана сума  + срок на депозита * ((депозирана сума * годишен лихвен процент ) / 12)
# Вход
# От конзолата се четат 3 реда:
# 1.	Депозирана сума – реално число в интервала [100.00 … 10000.00]
# 2.	Срок на депозита (в месеци) – цяло число в интервала [1…12]
# 3.	Годишен лихвен процент – реално число в интервала [0.00 …100.00]
# депозирана сума  + срок на депозита * ((депозирана сума * годишен лихвен процент ) / 12)
deposit_amount = float(input())
term_of_the_deposit = int(input())
annual_interest_rate = float(input())

accrued_interest = deposit_amount * (annual_interest_rate / 100)
interest_for_month = accrued_interest / 12
final_price = deposit_amount + (term_of_the_deposit * interest_for_month)


print(final_price)




